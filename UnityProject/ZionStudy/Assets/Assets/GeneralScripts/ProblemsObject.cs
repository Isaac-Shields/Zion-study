

public class problemObj
{
    private string problem;
    private string answer;

    public problemObj()
    {

    }

    public string getProblem()
    {
        return problem;
    }

    public string getAnswer()
    {
        return answer;
    }

    public void setProblem(string p)
    {
        problem = p;
    }

    public void setAnswer(string a)
    {
        answer = a;
    }
}